#include "libft.h"

int		ft_putxchar(char c, int n, int *ret)
{
	int	i;

	i = 0;
	if (n < 0)
		return (0);
	while (n--)
	{	
		ft_putchar(c);
		*ret += 1;
		i++;
	}
	return (i);
}
