#include "libft.h"

char	*ft_strchrfull(const char *s, char *c)
{
	int		i;
	int		j;

	j = 0;
	i = 0;
	while (c[j])
	{	
		while (s[i])
		{
			if (s[i] == c[j])
				return (&c[j]);
			i++;
		}
		i = 0;
		j++;
	}
	return (NULL);
}
