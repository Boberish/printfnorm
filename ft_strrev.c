#include "libft.h"

void	ft_strrev(char *s)
{
	char	temp;
	char	*e;
	int		len;

	len = ft_strlen(s);
	e = s + len - 1;
	while (s < e)
	{
		temp = *s;
		*s = *e;
		*e = temp;
		s++;
		e--;
	}
}
