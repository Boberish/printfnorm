#include "libft.h"

int	ft_intlen(intmax_t n)
{
	int i;

	i = 0;
	if (n == 0)
		return (1);
	if (n == -2147483648)
		return (11);
	if (n < 0)
	{
		n *= -1;
	}
	while (n)
	{
		i++;
		n /= 10;
	}
	return (i);
}
